def gated_fusion(layer, y1, y2):
    gates = nn.Conv2d(layer.shape[1], 2, 3, 1, 1, bias=False)
    gates = gates(layer) # find the weights
    gated_y = y1 * gates[:, [0], :, :] + y2 * gates[:, [1], :, :] # multiply the weights with the individual maps and combine them
    return gated_y

def blockUNet(in_c, out_c, name, transposed=False, bn=False, relu=True, dropout=False):
  block = nn.Sequential()
  if relu:
    block.add_module('%srelu' % name, nn.ReLU(inplace=True))
  else:
    block.add_module('%sleakyrelu' % name, nn.LeakyReLU(0.2, inplace=True))
  if not transposed: # make the dense blocks with dilated convolution so as to get more context
    block.add_module('%sconv2' % name, nn.Conv2d(in_c, out_c, 4, 2, 1, bias=False))
  else:
    block.add_module('%stconv' % name, nn.ConvTranspose2d(in_c, out_c, 4, 2, 1, bias=False))
  if bn:
    block.add_module('%sbn' % name, nn.InstanceNorm2d(out_c))
  if dropout:
    block.add_module('%sdropout' % name, nn.Dropout2d(0.5, inplace=True))
  return block


